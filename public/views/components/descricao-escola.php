    <h1 class="titulo-escola">Conheça nossa escola</h1>
    <p class="paragrafo-escola">A Etec de Monte Mor foi criada em fevereiro de 2009, com uma perspectiva de escola “nova
        e diferente” em sua
        qualidade e excelência de ensino. Após mais de dez anos, vários desafios foram vencidos e sua identidade é
        reconhecida no município e região, pautada por respeito e, acima de tudo, por comprometimento com a Educação do
        país, especialmente em nível estadual. Tem sido constante a busca por melhorias, permitindo novos desafios,
        alinhamento dos objetivos e proposta do ponto de vista pedagógico e de gestão.
        Somos uma instituição de ensino que tem por característica o fomento da ciência e da tecnologia através do
        incentivo aos seus estudantes no processo de pesquisa e desenvolvimento de projetos inovadores e empreendedores.
        Nossa escola está fundamentada nas leis e diretrizes governamentais e institucionais que regem a Educação
        Técnica Profissional, preparando os estudantes para a vida social, profissional e acadêmica por meio do ensino
        teórico e prático baseado em valores e atitudes, incentivando-o ao aprendizado contínuo.
        Democrática e com ambiente colaborativo a Escola permite aos discentes traçar novos projetos de vida, assumindo
        responsabilidades através da autonomia, e aos docentes e colaboradores técnico-administrativos é dado o
        incentivo para que cresçam pessoalmente e profissionalmente contribuindo assim com o crescimento da Unidade
        Escolar.
        Com esse enfoque, pretende-se um trabalho que trate de questões sobre: a ética; o apreço à tolerância e combate
        a quaisquer formas de discriminação; o pluralismo de ideias; a valorização das ideias; a experiência de mundo
        dos integrantes da comunidade escolar; as concepções pedagógicas; a valorização do docente e a formação
        continuada do profissional da educação.Participante de várias Feiras e Mostras Tecnológicas e Científicas nos
        últimos anos, a Etec de Monte Mor teve seu reconhecimento nacional ao vencer a categoria Mérito Institucional do
        Prêmio Jovem Cientista, edição 2013, promovido pelo Ministério de Ciência, Tecnologia e Inovação e o CNP em
        parceria com a Fundação Roberto Marinho, Grupo Gerdau e BG Brasil, que premia anualmente estudantes e escolas
        que promovem a pesquisa científica.</p>
